function sendMail(e) {
    e.preventDefault();
    const f = e.target;
    const to = 'allison.vargas2025@mottach.superate.org.pa';
    const subject = encodeURIComponent(f.subject.value || 'Contacto desde sitio web');
    const bodyLines = [
        `Nombre: ${f.firstName.value} ${f.lastName.value}`,
        `Email: ${f.email.value}`,
        '',
        `Mensaje:`,
        f.message.value
    ];
    const body = encodeURIComponent(bodyLines.join('\n'));
    // Abre el cliente de correo (Outlook si está configurado como predeterminado)
    window.location.href = `mailto:${to}?subject=${subject}&body=${body}`;
}

